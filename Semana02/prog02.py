# Comando de módulo:
print(abs(-3))

# Comando de arredondamento:
print(round(3.75)) 

#Arredondando para 1 casa:
print(round(3.75,1))

# Comparações:
# Igualdade:   
num_1 = 3
num_2 = 2
print(num_1 == num_2)

# Desigualdade: 
print(num_1!=num_2)

# Maior que:
print(num_1 > num_2)

# Menor que:
print(num_1 < num_2)

# Maior ou igual:
print(num_1 >= num_2)

# Menor ou igual: 
print(num_1 <= num_2)

