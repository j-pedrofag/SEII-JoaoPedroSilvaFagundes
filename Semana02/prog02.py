# Arithmetic Operators:
# Adição:      
print(3 + 2)

# Subtração:   
print(3 - 2)

# Multiplicação: 
print(3 * 2)

# Divisão:
print(3 / 2)

# Floor divisão:
print(3 // 2)

# Exponencial:
print(3 ** 2)

# Resto da divisão:
print(3 % 2)

# Implementações:
